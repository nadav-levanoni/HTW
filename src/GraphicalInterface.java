    public GraphicalInterface(int randomMap) {

        //TODO loop through array, create a string of the location (x + y); create a sprite object and add it to the hash map. key is the x + y string.
        //TODO ripoudya@microsoft.com

        thread = new Thread(this);
        image = new BufferedImage(640, 480, BufferedImage.TYPE_INT_RGB);
        pixels = ((DataBufferInt)image.getRaster().getDataBuffer()).getData();

        spriteRandomizer = new int[106];

        textures = new ArrayList<Texture>();


        textures.add(Texture.wood);
        textures.add(Texture.brick);
        textures.add(Texture.bluestone);
        textures.add(Texture.stone);


        
        if (randomMap == 1) {
            current = map1;
        }

        if (randomMap == 2) {
            current = map2;
        }

        if (randomMap == 3) {
            current = map3;
        }
        if (randomMap == 4) {
            current = map4;
        }
        if (randomMap == 5) {
            current = map5;
        }

        camera = new Camera(4.5, 4.5, 1, 0, 0, -.66);
        screen = new Screen(current, mapWidth, mapHeight, textures, 640, 480);
        addKeyListener(camera);
        setSize(640, 480);
        setResizable(false);
        setTitle("Faze Clan Game");
        setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        setBackground(Color.black);
        setLocationRelativeTo(null);
        setVisible(true);
        start();
    }
    private synchronized void start() {
        running = true;
        thread.start();
    }
    public synchronized void stop() {
        running = false;
        try {
            thread.join();
        } catch(InterruptedException e) {
            e.printStackTrace();
        }
    }
    public void render() {
        BufferStrategy bs = getBufferStrategy();
        if(bs == null) {
            createBufferStrategy(3);
            return;
        }
        Graphics g = bs.getDrawGraphics();
        g.drawImage(image, 0, 0, image.getWidth(), image.getHeight(), null);
        bs.show();
    }
    public void run() {
        long lastTime = System.nanoTime();
        final double ns = 1000000000.0 / 60.0;//60 times per second
        double delta = 0;
        requestFocus();
        while(running) {
            long now = System.nanoTime();
            delta = delta + ((now-lastTime) / ns);
            lastTime = now;
            while (delta >= 1)//Make sure update is only happening 60 times a second
            {
                //handles all of the logic restricted time
                screen.update(camera, pixels);
                camera.update(current);
                delta--;
            }
            render();//displays to the screen unrestricted time
        }
    }


